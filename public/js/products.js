// ── Helper: format price in Naira ────────────────────────────────
window.JO_PRICE = function(amount) {
    return '₦' + Number(amount).toLocaleString('en-NG');
};

// ── Helper: get products by category ─────────────────────────────
window.JO_FILTER = function(category, query) {
    let results = window.JO_PRODUCTS;
    if (category && category !== 'all') {
        const c = category.toLowerCase();
        results = results.filter(p =>
            p.category.toLowerCase() === c ||
            p.sub.toLowerCase() === c ||
            // special groupings
            (c === 'men' && p.isMen) ||
            (c === 'women' && !p.isMen) ||
            (c === 'polo' && p.sub === 'Polo') ||
            (c === 'joggers' && p.sub === 'Joggers') ||
            (c === 'luggage' && (p.sub === 'Luggage' || p.sub === 'Travel')) ||
            (c === 'petite' && p.isPetite) ||
            (c === 'kids' && p.sub === 'Kids') ||
            (c === 'suits' && p.sub === 'Suits') ||
            (c === 'traditional' && p.sub === 'Traditional') ||
            (c === 'sale' && p.originalPrice && p.originalPrice > p.price) ||
            (c === 'new' && p.isNew)
        );
    }
    if (query) {
        const q = query.toLowerCase();
        results = results.filter(p =>
            p.name.toLowerCase().includes(q) ||
            p.category.toLowerCase().includes(q) ||
            p.sub.toLowerCase().includes(q) ||
            p.brand.toLowerCase().includes(q) ||
            (p.tags && p.tags.some(t => t.includes(q))) ||
            (p.desc && p.desc.toLowerCase().includes(q)) ||
            (p.isPetite && (q.includes('petite') || q.includes('small') || q.includes('short'))) ||
            (p.isMen && (q.includes('men') || q.includes('male') || q.includes('man') || q.includes('gent'))) ||
            (p.sub === 'Traditional' && (q.includes('native') || q.includes('nigerian') || q.includes('agbada') || q.includes('senator') || q.includes('kaftan')))
        );
    }
    return results;
};
